package day03_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone\nMy name is Ibrahim, I am from Turkey");
        System.out.println("*******************************************");
        System.out.println("Hello Everyone\n" +
                "My name is Ibrahim, I am from Turkey");
        System.out.println("*******************************************");
        System.out.println("Hello Everyone");
        System.out.println("My name is Ibrahim, I am from Turkey");


        System.out.println("*******************************************");


        System.out.println("\tToday I learned Java");
        System.out.println("Today I learned Java");

        System.out.println("*******************************************");


        System.out.println("\\");
        System.out.println("/");
        System.out.println("\'");
        System.out.println("\"");
        System.out.println("'");
        System.out.println("My favorite show is \"Game Of Thrones\"");




    }

}
