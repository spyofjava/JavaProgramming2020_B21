package day19_ForLoop;

public class ForLoopIntro {
    public static void main(String[] args) {

        for(int i=1;i<=25;i++){
            System.out.println("hello");
        }
        System.out.println("=====================");


        String name="Cybertek School";
        for (int i=5;i>0;i--){
            System.out.println(name);

        }



    }
}
