package day21_WhileLoops;

public class WhileLoopPractice {
    public static void main(String[] args) {

        for(int i =1; i <= 10; i++){
            System.out.println(i);
        }

        System.out.println("=============================");

        int j =1;
        while(j <= 10){
            // j++; // 2-11
            System.out.println(j);

            j++; // 1-10
        }

    }
}
