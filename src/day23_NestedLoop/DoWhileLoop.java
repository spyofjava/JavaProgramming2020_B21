package day23_NestedLoop;

public class DoWhileLoop {
    public static void main(String[] args) {


        boolean contition=true;
        while (contition){// think first
            System.out.println("Hello Batch 21");
        }

        System.out.println("==================================");

        do{
            System.out.println("Hello Batch 21");
        }while (contition); //do first, think





    }
}
